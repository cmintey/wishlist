<script lang="ts">
    import { enhance } from "$app/forms";
    import ItemForm from "$lib/components/wishlists/ItemForm.svelte";
    import { getToastStore } from "@skeletonlabs/skeleton";
    import type { PageProps } from "./$types";
    import { getFormatter } from "$lib/i18n";
    import { errorToast } from "$lib/components/toasts";

    const { data }: PageProps = $props();

    const t = getFormatter();
    const toastStore = getToastStore();

    let saving = $state(false);
</script>

{#if data.item}
    <form
        enctype="multipart/form-data"
        method="POST"
        use:enhance={() => {
            saving = true;
            return async ({ result, update }) => {
                saving = false;
                if (result.type === "error") {
                    errorToast(toastStore, (result.error?.message as string) || $t("general.oops"));
                    return;
                } else {
                    toastStore.trigger({
                        message: $t("wishes.updated-success"),
                        autohide: true,
                        timeout: 5000
                    });
                    update();
                }
            };
        }}
    >
        <ItemForm buttonText={$t("general.save")} item={data.item} lists={data.lists} {saving} />
    </form>
{/if}

<svelte:head>
    <title>{$t("wishes.edit-wish")}</title>
</svelte:head>
